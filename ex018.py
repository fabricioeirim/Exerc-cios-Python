import math
a = float(input('Digite o valor do angulo qualquer: '))
sen = math.sin(math.radians(a))
print('Para o angulo {} o seno dele é {:.2f}'.format(a, sen))
cos = math.cos(math.radians(a))
print('Para o angulo {} o seno dele é {:.2f}'.format(a, cos))
tan = math.tan(math.radians(a))
print('Para o angulo {} o seno dele é {:.2f}'.format(a, tan))